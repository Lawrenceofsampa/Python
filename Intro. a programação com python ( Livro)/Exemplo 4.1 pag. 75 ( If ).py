while True:
    
    a = int(input('Entre com o valor A: '))
    b = int(input('Entre com o valor B: '))

    if a > b:
        print('A  maior que B')

    if b > a:
        print('B  maior que A')

    if a == b:
        print('A e B são iguais!')

