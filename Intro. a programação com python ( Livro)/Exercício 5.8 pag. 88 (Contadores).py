while True:
    print('-------------Multiplicação de dois números através de contadores----------------------')
    Primeiro = int(input('Entre com o primeiro número: '))
    Segundo = int(input('Entre com segundo número: '))
    x = 1
    y = 0
    while x <= Segundo:
        y = y + Primeiro
        x = x + 1
    print('O resultado da multiplicação é: ', y)




